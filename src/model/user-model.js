import mongoose from "mongoose";

// Mendefinisikan skema User
const userModel = new mongoose.Schema({
  fullname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

// Membuat model User berdasarkan skema
const User = mongoose.model("users", userModel);

export default User;
